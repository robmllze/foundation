class WelcomeScreenController extends TWelcomeScreenController {
  //
  //
  //

  WelcomeScreenController(super.screen, super.state);

  //
  //
  //

  final pCounter = Pod<int>(-1);

  //
  //
  //

  void incrementCounter() {
    this.pCounter.update((final value) => value + 1);
  }

  //
  //
  //

  @override
  void initController() {
    this.pCounter.set(0);
    super.initController();
  }

  //
  //
  //

  @override
  void dispose() {
    // Be sure to dispose all pods here.
    this.pCounter.dispose();
    super.dispose();
  }
}

