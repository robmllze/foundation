void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  setPathUrlStrategy();
  startApp();
  runApp(const MainWidget());
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

void startApp() async {
  // 1. Create a service environment to interact with the backend.
  final serviceEnvironment = await createFirebaseServiceEnvironment(
    {
      ServiceEnvironmentType.TEST: firebase_options_test.DefaultFirebaseOptions.currentPlatform,
    }[ServiceEnvironment.currentServiceEnvironment]!,
  );

  // Create an app environment to handle the global state of the app.
  app = AppEnvironment(serviceEnvironment);

  // Create an app session to handle the state of the current StateBroker.
  final appSession = AppSession(serviceEnvironment);

  // Initialize the app with the StateBroker.
  app.initApp(appSession);

  await pAppSession.set(appSession);
}

